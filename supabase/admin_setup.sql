INSERT INTO storage.buckets (id, name, public)
VALUES ('photocards', 'photocards', true)
ON CONFLICT (id) DO NOTHING;

DROP POLICY IF EXISTS "admin upload photocards" ON storage.objects;
CREATE POLICY "admin upload photocards"
  ON storage.objects FOR INSERT TO authenticated
  WITH CHECK (
    bucket_id = 'photocards'
    AND EXISTS (SELECT 1 FROM public.user_profiles p WHERE p.id = auth.uid() AND p.is_admin)
  );

DROP POLICY IF EXISTS "admin update photocards" ON storage.objects;
CREATE POLICY "admin update photocards"
  ON storage.objects FOR UPDATE TO authenticated
  USING      (bucket_id = 'photocards' AND EXISTS (SELECT 1 FROM public.user_profiles p WHERE p.id = auth.uid() AND p.is_admin))
  WITH CHECK (bucket_id = 'photocards' AND EXISTS (SELECT 1 FROM public.user_profiles p WHERE p.id = auth.uid() AND p.is_admin));

DROP POLICY IF EXISTS "admin delete photocards" ON storage.objects;
CREATE POLICY "admin delete photocards"
  ON storage.objects FOR DELETE TO authenticated
  USING (bucket_id = 'photocards' AND EXISTS (SELECT 1 FROM public.user_profiles p WHERE p.id = auth.uid() AND p.is_admin));

DROP POLICY IF EXISTS "public read photocards" ON storage.objects;
CREATE POLICY "public read photocards"
  ON storage.objects FOR SELECT TO public
  USING (bucket_id = 'photocards');
