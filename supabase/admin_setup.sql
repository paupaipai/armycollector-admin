ALTER TABLE public.albums ADD COLUMN IF NOT EXISTS short_name         text;
ALTER TABLE public.albums ADD COLUMN IF NOT EXISTS release_date       date;

CREATE TABLE IF NOT EXISTS public.card_categories (
  id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        text    NOT NULL,
  short_name  text    NOT NULL,
  description text,
  color       text             DEFAULT '#E040A0',
  sort_order  integer          DEFAULT 0
);

CREATE TABLE IF NOT EXISTS public.album_versions (
  id         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  album_id   integer NOT NULL REFERENCES public.albums (id) ON DELETE CASCADE,
  name       text    NOT NULL,
  short_name text,
  sort_order integer DEFAULT 0
);

-- ============================================================
-- 4) TABLAS CON DEPENDENCIAS DE SEGUNDO NIVEL
-- ============================================================

CREATE TABLE IF NOT EXISTS public.card_sets (
  id          bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  album_id    bigint      NOT NULL REFERENCES public.albums (id),
  version_id  bigint               REFERENCES public.album_versions (id),
  category_id bigint               REFERENCES public.card_categories (id),
  name        text        NOT NULL,
  short_name  text        NOT NULL,
  description text,
  retailer    text,
  round       text,
  sort_order  integer     NOT NULL DEFAULT 0,
  is_active   boolean     NOT NULL DEFAULT true,
  created_at  timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.cards (
  id               integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  album_id         integer     NOT NULL REFERENCES public.albums (id),
  version_id       integer              REFERENCES public.album_versions (id),
  category_id      integer     NOT NULL REFERENCES public.card_categories (id),
  member           text        NOT NULL,
  member_full_name text,
  member_emoji     text,
  retailer         text,
  card_name        text        NOT NULL,
  code             text        NOT NULL UNIQUE,
  image_path       text,
  rarity           text                 DEFAULT 'Common'
                               CHECK (rarity = ANY (ARRAY['Common','Rare','Ultra Rare','Limited'])),
  is_group         boolean              DEFAULT false,
  is_blurred       boolean              DEFAULT false,
  release_date     date,
  notes            text,
  created_at       timestamptz          DEFAULT now(),
  is_visible       boolean              DEFAULT false,
  card_set_id      bigint               REFERENCES public.card_sets (id)
);

-- Columna nueva en caso de que cards ya exista sin ella
ALTER TABLE public.cards ADD COLUMN IF NOT EXISTS card_set_id bigint REFERENCES public.card_sets (id);

-- Para la app móvil — seguimiento de colección por usuario
CREATE TABLE IF NOT EXISTS public.user_cards (
  id             integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id        uuid        NOT NULL REFERENCES auth.users (id),
  card_id        integer     NOT NULL REFERENCES public.cards (id),
  status         public.card_status  DEFAULT 'pending',
  quantity       integer              DEFAULT 1,
  acquired_date  timestamptz,
  trade_open     boolean              DEFAULT false,
  notes          text,
  created_at     timestamptz          DEFAULT now(),
  updated_at     timestamptz          DEFAULT now(),
  duplicate_count integer    NOT NULL DEFAULT 0
);

